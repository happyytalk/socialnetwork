export const getRandomNameByLanguage = (language, roomId) => {
    const langData = CREATOR_NAMES[language] || CREATOR_NAMES.Default;
    const allNames = [...langData.Male, ...langData.Female];
    
    // Seeded random selection
    const seed = typeof roomId === 'number' ? roomId : (String(roomId).split('').reduce((acc, char) => acc + char.charCodeAt(0), 0));
    const index = seed % allNames.length;
    return allNames[index];
};
